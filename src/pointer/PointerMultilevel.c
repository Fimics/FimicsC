#include <printf.h>

/**
 * 多级指针
 */
void pointerMultilevel(){

    int a = 10;
    int * p = &a;
    //1.二级指针存放一级指针地址
    int ** q = &p;

    //二级批针 前面一个*,代表一级指针的值
    *q = p= &a;
    //二级指针 前面两个**  代表指针指向一级指针指向地址的值
    **q = *p = a;
   // printf("%d\n",**q);

    int *** w = &q;



    /**
     *     1.二级指针存放一级指针地址
     *     2.每加一个*向原始值找一步,维度降1
     *
     *       ----------------------------------------------------------------------------------
     * 定义: | int a =10   | int *p =&a  | int **pp =&p | int ***ppp = &pp |int ****pppp = &ppp|
     *       ----------------------------------------------------------------------------------
     * 内值: |   10        |  0xff00     |  0xffaa      |    0xffbb        |   0xffcc          |
     *       ----------------------------------------------------------------------------------
     * 内址: |  0xff00     |  0xffaa     |  0xffbb      |    0xffcc        |   0xffdd          |
     *      -----------------------------------------------------------------------------------
     *
     *      pppp=&ppp
     *      *pppp = ppp =&pp
     *      **pppp = *ppp =pp = &p
     *      ***pppp = **ppp =*pp =p = &a;
     *      ****pppp =a;
     *
     *
     *
     *       --------------------------------------------------------------
     * 定义: | int a =10   | int *p =&a  | int **q      | int ***w   = &pp |
     *       --------------------------------------------------------------
     * 内值: |   10        |  0xff00     |  0xffaa      |    0xffbb        |
     *       --------------------------------------------------------------
     * 内址: |  0xff00     |  0xffaa     |  0xffbb      |    0xffcc        |
     *      ---------------------------------------------------------------
     *
     *       w = &q;
     *       *w = q =&p;
     *       ** w = p = &a;
     *       ***w = a;
     */

}